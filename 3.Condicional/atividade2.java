/*Faça um algoritmo para ler um número inteiro e
 * apresentá-lo, caso não seja maior que 3.
 */
public class atividade2 {
    public static void main(String[] args) {
        int numero = 2; // Exemplo de número
        if (numero <= 3) {
            System.out.println("O número é: " + numero);
        } else {
            System.out.println("O número é maior que 3.");
        }
    }
}// O código verifica se o número é maior que 3 e imprime a mensagem correspondente.